#include "libft.h"

int	ft_toupper(int *str)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[a] >= 97 && str[a] <= 122)
			str[a] -= 32;
		a++;
	}
	return (str);
}
